class MaskNoneProvidedFrameLogitsProcessor(LogitsProcessor):
    def __init__(self, attention_mask, vocab_size):
        super().__init__()
        self.attention_mask = attention_mask
        self.vocab_size = vocab_size
    
    @torch.no_grad()
    def __call__(self, input_ids: torch.LongTensor, scores: torch.FloatTensor) -> torch.FloatTensor:
        bsz = scores.size(0)
        text_mask = torch.ones(bsz, self.vocab_size, device=scores.device)
        pad_only_mask = torch.zeros(
            bsz,
            (scores.size(-1) -  self.vocab_size - self.attention_mask.size(-1)),
            device=scores.device,
        )
        mask = torch.cat(
            (
                text_mask,
                self.attention_mask.to(scores.dtype),
                pad_only_mask
            ),
            dim=-1,
        ).to(torch.bool)
        scores[mask == False] = float("-inf")
        return scores


